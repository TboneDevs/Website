    def _is_bad_response(self, r: Optional[Dict]) -> bool:
        if r is None:
            return True
        if isinstance(r, dict) and "raw" in r:
            raw = str(r.get("raw", ""))
            if "404" in raw or "Page not found" in raw or "Error" in raw[:80]:
                return True
        return False

    async def _request_once(self, url: str, payload: Dict, headers: Dict, proxy: Optional[str]) -> Optional[Dict]:
        try:
            timeout = aiohttp.ClientTimeout(total=25, connect=10)
            connector = aiohttp.TCPConnector(ssl=False, limit=50)
            h = {**self.base_headers, **(headers or {})}
            async with aiohttp.ClientSession(timeout=timeout, connector=connector) as s:
                async with s.post(url, json=payload or {}, headers=h, proxy=proxy) as resp:
                    text = await resp.text()
                    if resp.status == 404 or "Page not found" in text[:200]:
                        return {"raw": text, "_status": 404}
                    try:
                        data = json.loads(text)
                        if isinstance(data, dict):
                            data["_status"] = resp.status
                        return data
                    except Exception:
                        return {"raw": text, "_status": resp.status}
        except Exception as e:
            log.warning(f"Request fail proxy={proxy}: {e}")
            return None

    async def _request(self, url: str, payload: Dict = None,
                       headers: Dict = None, params: Dict = None,
                       max_attempts: int = 12) -> Optional[Dict]:
        """Try multiple proxies until success or attempts exhausted."""
        last = None
        attempts = max(1, min(max_attempts, max(len(PROXIES), 1)))
        # Also try direct (no proxy) as last resorts if all proxies fail? No - direct is blocked on Railway
        for i in range(attempts):
            proxy = await self._next_proxy()
            r = await self._request_once(url, payload or {}, headers or {}, proxy)
            last = r
            if r is None:
                continue
            if self._is_bad_response(r):
                continue
            # success-ish JSON
            if isinstance(r, dict) and "raw" not in r:
                return r
            # sometimes result embedded
            if isinstance(r, dict) and ("result" in r or "idToken" in r or "error" in r):
                return r
        return last

    async def _refresh_token(self, uid: int) -> Tuple[bool, str]:
        td = self.get_token_data(uid)
        if not td:
            return False, "NO_TOKEN"
        rt = td.get("refresh_token")
        em = td.get("email")
        pw = td.get("password")

        if rt:
            r = await self._request(
                f"https://securetoken.googleapis.com/v1/token?key={FK}",
                {"grant_type": "refresh_token", "refresh_token": rt}
            )
            if r and r.get("id_token"):
                self.update_token(uid, r["id_token"], r.get("refresh_token", rt))
                return True, "OK"

        if em and pw:
            res = await self.account_login(em, pw)
            if res.get("ok"):
                self.update_token(uid, res["auth"], res.get("refresh_token", ""))
                return True, "OK"
        return False, "REFRESH_FAILED"

